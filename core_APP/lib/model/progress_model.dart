class ProgressData {
  final double overallProgress;
  final int conceptsLearned;
  final int totalConcepts;
  final int reelsWatched;
  final int totalReels;
  final int mockTests;
  final int accuracy;

  ProgressData({
    required this.overallProgress,
    required this.conceptsLearned,
    required this.totalConcepts,
    required this.reelsWatched,
    required this.totalReels,
    required this.mockTests,
    required this.accuracy,
  });
}